let Marks;
Marks = (prompt("Enter Your Marks"))
if (Marks >= 90) {
    alert(Marks + " A+")
}
else if (Marks >= 80 && Marks <= 89) {
    alert(Marks + " A")
}
else if (Marks >= 70 && Marks <= 79) {
    alert(Marks + " B")
}
else if (Marks >= 60 && Marks <= 69) {
    alert(Marks + " C")
}
else {
    alert(Marks + " Fail")
}

